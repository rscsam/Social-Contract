public class InterestProfile {
    private boolean music, food, movies, videoGames, sports, memes;

    public boolean getMusic() {
        return music;
    }

    public void setMusic(boolean music) {
        this.music = music;
    }

    public boolean getFood() {
        return food;
    }

    public void setFood(boolean food) {
        this.food = food;
    }

    public boolean getMovies() {
        return movies;
    }

    public void setMovies(boolean movies) {
        this.movies = movies;
    }

    public boolean getVideoGames() {
        return videoGames;
    }

    public void setVideoGames(boolean videoGames) {
        this.videoGames = videoGames;
    }

    public boolean getSports() {
        return sports;
    }

    public void setSports(boolean sports) {
        this.sports = sports;
    }

    public boolean getMemes() {
        return memes;
    }

    public void setMemes(boolean memes) {
        this.memes = memes;
    }
}
